package Class6;

public class WhileLoopTask {

    public static void main(String[] args) {

        int number=1;
        while (number<101){
            System.out.println(number);
            number++;
        }
        System.out.println("********");

        int number2=1;
        while (number2<16){
            System.out.println(number2);
            number2+=2;
        }
        System.out.println("******");

        int number3=5;
        while(number3<51){
            System.out.println(number3);
            number3+=5;
        }
    }
}
